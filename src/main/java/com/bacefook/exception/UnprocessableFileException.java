package com.bacefook.exception;

public class UnprocessableFileException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4539436154804325422L;

	public UnprocessableFileException() {
		super();
	}

	public UnprocessableFileException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public UnprocessableFileException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UnprocessableFileException(String arg0) {
		super(arg0);
	}

	public UnprocessableFileException(Throwable arg0) {
		super(arg0);
	}

	
	
	
	
}
